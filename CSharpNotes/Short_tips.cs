// 2. Matrix diagonals------------------------------------------------------------------------
public static IEnumerable<T> PrimaryDiagonal<T>(IEnumerable<T[]> values)
{
    return values.Select((x, i) => x[i]);
}

public static IEnumerable<T> SecondaryDiagonal<T>(IEnumerable<T[]> values)
{
    return values.Reverse().Select((x, i) => x[i]);
}

